package com.idfor.kat.tools.katjobmanagerbundle.beans;

import java.util.List;

/**
 * Bean used to store properties of deployed TALEND binaries
 */
public class BinariesProperties {

    private String name;
    private String version;
    private String project;
    private List<String> contextList;

    public BinariesProperties(String name, String version, String project, List<String> contextList){
        this.name = name;
        this.version = version;
        this.project = project;
        this.contextList = contextList;
    }

    public String getName() {
        return name;
    }

    public String getVersion() {
        return version;
    }

    public String getProject() {
        return project;
    }

    public List<String> getContextList() {
        return contextList;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setVersion(String version) {
        this.version = version;
    }

    public void setProject(String project) {
        this.project = project;
    }

    public void setContextList(List<String> contextList) {
        this.contextList = contextList;
    }

    @Override
    public String toString() {
        return super.toString();
    }
}